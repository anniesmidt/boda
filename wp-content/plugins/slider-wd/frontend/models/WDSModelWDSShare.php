  public function get_slide_row_data($image_id) {
    global $wpdb;
    $row = $wpdb->get_row($wpdb->prepare('SELECT * FROM ' . $wpdb->prefix . 'wdsslide WHERE published=1 AND id="%d"', $image_id));
    return $row;
